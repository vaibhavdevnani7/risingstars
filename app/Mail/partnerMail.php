    {
        $this->data = $mail_data;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        $data = $this->data;
        return $this->view('emails.partner-acknowledgement',compact('data'))->subject('Letâ€™s BUIDL the Indian Blockchain Ecosystem together - Rising Stars Accelerator');
    }
}
